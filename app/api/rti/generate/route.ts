export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    const { tender_id, citizen_name, citizen_address, citizen_email } = body;

    if (!citizen_name || !citizen_address) {
      return NextResponse.json({ success: false, error: 'Name and address are required' }, { status: 400 });
    }

    const isDemoMode = process.env.NEXT_PUBLIC_DEMO_MODE === 'true';
    const citizen = { name: citizen_name, address: citizen_address, email: citizen_email || '' };

    let tender = AIIMS_TENDER;

    if (!isDemoMode && tender_id) {
      // Real mode: fetch from Supabase
      const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL;
      const supabaseKey = process.env.SUPABASE_SERVICE_ROLE_KEY || process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY;
      if (supabaseUrl && supabaseKey) {
        try {
          const res = await fetch(`${supabaseUrl}/rest/v1/tenders?id=eq.${tender_id}&select=*`, {
            headers: { 'apikey': supabaseKey, 'Authorization': `Bearer ${supabaseKey}` },
          });
          if (res.ok) {
            const tenders = await res.json();
            if (tenders.length > 0) {
              const t = tenders[0];
              tender = {
                ...AIIMS_TENDER,
                tender_id: t.id || tender_id,
                title: t.title || AIIMS_TENDER.title,
                ministry: t.ministry || AIIMS_TENDER.ministry,
                estimated_value: t.estimated_value ? t.estimated_value / 1_00_00_000 : AIIMS_TENDER.estimated_value,
                risk_score: t.risk_score || AIIMS_TENDER.risk_score,
                status: t.status || AIIMS_TENDER.status,
              };
            }
          }
        } catch { /* fallback to demo */ }
      }
    }

    const rtiHtml = generateRTIHtml(citizen, tender);
    const refNo = `RTI-TS-${(tender_id || 'MoH003').replace(/[^A-Z0-9]/g, '')}-${Date.now().toString(36).toUpperCase()}`;

    return NextResponse.json({
      success: true,
      data: {
        reference_number: refNo,
        rti_letter_html: rtiHtml,
        tender_id: tender.tender_id,
        ministry: tender.ministry,
        ministry_address: tender.ministry_address,
        generated_at_ist: new Date().toLocaleString('en-IN', { timeZone: 'Asia/Kolkata' }),
        filing_instructions: {
          offline: [
            'Print 2 copies of this RTI application',
            'Attach ₹10 Indian Postal Order or Court Fee Stamp',
            `Send by registered post to: PIO, ${tender.ministry}, ${tender.ministry_address}`,
            'Keep one copy and the postal receipt for your records',
            'You must receive a response within 30 days',
          ],
          online: [
            'Visit rtionline.gov.in',
            `Select Ministry: "${tender.ministry}"`,
            'Copy-paste the RTI text into the application form',
            'Pay ₹10 online fee',
            'Save the registration number for tracking',
          ],
          appeal: [
            'If denied or no response in 30 days → First Appeal to departmental First Appellate Authority',
            'If First Appeal fails → Second Appeal to Central Information Commission (CIC)',
            'CIC can impose ₹25,000 penalty on non-compliant PIO',
          ],
        },
      },
    });
  } catch {
    return NextResponse.json({ success: false, error: 'Failed to generate RTI' }, { status: 500 });
  }
}
