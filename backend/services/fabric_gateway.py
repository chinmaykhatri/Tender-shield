logger = logging.getLogger("tendershield.fabric.gateway")
IST = timezone(timedelta(hours=5, minutes=30))

# Path to crypto material (generated by Fabric CA or cryptogen)
CRYPTO_BASE = Path(__file__).parent.parent.parent / "fabric-crypto"
CONNECTION_PROFILE_PATH = Path(__file__).parent.parent.parent / "fabric-config" / "connection-profile.json"

# Try importing Fabric Gateway SDK — graceful fallback if not installed
_GATEWAY_AVAILABLE = False
