"""
============================================================================
TenderShield — Real Hyperledger Fabric Gateway Service
============================================================================
Connects to a live Hyperledger Fabric 2.5 network via gRPC.
Submits real transactions, queries the real ledger, returns real TX IDs.

ARCHITECTURE:
  This module provides real Fabric connectivity via the Gateway SDK.
  It is loaded by fabric_service.py via the strategy pattern:
    - If Fabric is reachable → FabricGatewayService handles all calls
    - If Fabric is down      → FabricSQLiteService (legacy) handles

REQUIREMENTS:
  pip install grpcio grpcio-tools cryptography
  A running Fabric network with the TenderShield chaincode deployed.

CONNECTION:
  Uses Fabric Gateway peer protocol (gRPC on port 7051).
  Identity loaded from X.509 certificates generated by Fabric CA.
============================================================================
"""

